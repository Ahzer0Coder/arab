// Helper function to get questions by topic
function getQuestionsByTopic(topicId) {
    return quizData.questions.filter(q => q.topic === topicId);
}

// Helper function to get all MCQ questions
function getMCQQuestions() {
    return quizData.questions.filter(q => q.type === 'mcq');
}

// Helper function to get all True/False questions
function getTrueFalseQuestions() {
    return quizData.questions.filter(q => q.type === 'truefalse');
}

// Helper function to get random questions
function getRandomQuestions(array, count) {
    const shuffled = array.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(count, array.length));
}
